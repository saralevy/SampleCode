			<h2 class="entry-title"><a href="http://blog.symtrend.com/adl/?p=39" title="Permalink to Controlled Trial with Melatonin for Sleep in Children with Autism" rel="bookmark">Controlled Trial with Melatonin for Sleep in Children with Autism</a></h2>

			<div class="entry-meta">
				<span class="meta-prep meta-prep-author">Posted on</span> <a href="http://blog.symtrend.com/adl/?p=39" title="11:16 am" rel="bookmark"><span class="entry-date">December 17, 2012</span></a> <span class="meta-sep">by</span> <span class="author vcard"><a class="url fn n" href="http://blog.symtrend.com/adl/?author=1" title="View all posts by "></a></span>			</div><!-- .entry-meta -->

				<div class="entry-content">
				<p>A group from Vanderbilt department of Neurology conducted a control trial in which 24 children with autism and sleep difficulties were given supplemental melatonin (Natrol&reg;). The melatonin improved sleep latency in most children with doses at 1 or 3 mg. It was effective in week 1 of treatment and was well tolerated and safe &#8211; few if any side effects.</p>
<p><a href="http://www.mc.vanderbilt.edu/vhost/vbi/graduate/pdfs/melatonin%20dose-response%20trial.pdf">See the article.</a></p>
<p>Have you tried melatonin with your children?</p>
