			<div class="entry-meta">
				<span class="meta-prep meta-prep-author">Posted on</span> <a href="http://blog.symtrend.com/adl/?p=46" title="5:17 pm" rel="bookmark"><span class="entry-date">January 30, 2013</span></a> <span class="meta-sep">by</span> <span class="author vcard"><a class="url fn n" href="http://blog.symtrend.com/adl/?author=1" title="View all posts by "></a></span>			</div><!-- .entry-meta -->

				<div class="entry-content">
				<p>A post on the ADDitude website (Living Well with Attention Deficit) gives recommendations for what to do about behavior breakdowns. <a href="http://www.additudemag.com/adhd/article/5762.html" target="_blank">Check it out.</a></p>
<p>Recommends agreeing on calming plans, acknowledging anguish, &#8220;setting the bar&#8221;, deep breathing, getting punchy (punch safe object like a pillow), press buttons of a remote (like pressing emotions buttons), and asking for help.</p>
<p>If you want to track challenging behavior you can do this in the ADL app or in our other app: <a href="www.symtrend.com/nmm/" target="_blank">No More Meltdowns.</a></p>
